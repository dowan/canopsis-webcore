define([
    'app/application',
    'app/controller/crecords'
], function(Application, CrecordsController) {

    var controller = CrecordsController.extend({
        itemType: 'group',
        toolbar: [{
            title: 'Refresh',
            action: 'refresh',
            icon: 'refresh'
        },{
            title: 'Add',
            action: 'showAddForm',
            icon: 'plus-sign'
        },{
            title: 'Remove',
            action: 'remove',
            icon: 'trash'
        }],

        actions: {
            refresh: function() {
                this.set('content', this.store.findAll('group'));
            }
        }
    });

    Application.GroupsController = controller;

    return controller;
});
